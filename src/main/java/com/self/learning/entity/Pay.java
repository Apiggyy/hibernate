package com.self.learning.entity;

public class Pay {
    private Integer monthlySalary;
    private Integer yearlySalary;
    private Integer vocationDays;

    public Integer getMonthlySalary() {
        return monthlySalary;
    }

    public void setMonthlySalary(Integer monthlySalary) {
        this.monthlySalary = monthlySalary;
    }

    public Integer getYearlySalary() {
        return yearlySalary;
    }

    public void setYearlySalary(Integer yearlySalary) {
        this.yearlySalary = yearlySalary;
    }

    public Integer getVocationDays() {
        return vocationDays;
    }

    public void setVocationDays(Integer vocationDays) {
        this.vocationDays = vocationDays;
    }
}
